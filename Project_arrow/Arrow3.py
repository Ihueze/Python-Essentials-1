# ASCII Art Arrows in Python

# This script prints two versions of an ASCII art arrow:

## The **first arrow** is printed line by line.
## The **second version** places **two arrows side by side** by multiplying each string.

## ðŸ”¹ First Arrow (Single)


print("    *")
